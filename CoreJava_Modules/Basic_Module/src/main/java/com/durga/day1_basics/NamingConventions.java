public class NamingConventions {
    // Constants should be in uppercase, with words separated by underscores

    public static final int MAX_COUNT = 10000;

    public static void main(String[] args) {

        // Variable names should start with a lowercase letter and use camelCase

        int age = 25;
        double salaryPerHour = 50.75;
        String firstName = "John";
        boolean isEmployed = true;

        // Use meaningful names that describe the purpose of the variables
        int numberOfStudents = 50;
        double hourlyWage = 15.50;
        String lastName = "Doe";
        boolean hasJob = false;
    }
}
