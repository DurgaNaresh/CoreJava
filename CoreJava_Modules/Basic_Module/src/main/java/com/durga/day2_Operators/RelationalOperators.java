public class RelationalOperators {
    //    - Equal to (==): Checks if two operands are equal.
    //    - Not equal to (!=): Checks if two operands are not equal.
    //    - Greater than (>),
    //    - Less than (<),
    //    - Greater than or equal to (>=),
    //    - Less than or equal to (<=) : Perform comparison between operands.

    public static void main(String[] args) {

        System.out.println("Relational Operators Less than (<) :"+(45 < 89));
        System.out.println("Relational Operators  Greater than (>) :"+(45 > 89));
        System.out.println("Relational Operators  Less than or equal to (<=) :"+(45 <= 89));
        System.out.println("Relational Operators  Greater than or equal to (>=) :"+(45 >= 89));
        System.out.println("Relational Operators  Equal to (==) :"+(45 == 89));
        System.out.println("Relational Operators  Not equal to (!=) :"+(45 != 89));
    }
}
